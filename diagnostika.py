@router.callback_query(F.data == "quiz_start")
async def start_quiz(callback: CallbackQuery, state: FSMContext):
    await state.set_state(QuizStates.answering)
    await state.update_data(q_idx=0, scores={"А": 0, "Б": 0, "В": 0, "Г": 0})
    
    # Удаляем стартовое фото-сообщение, чтобы начать опрос в чистом виде
    await callback.message.delete()
    
    # Отправляем первый вопрос отдельным сообщением
    await send_first_question(callback.message.chat.id, 0)
    await callback.answer()

async def send_first_question(chat_id: int, q_idx: int):
    q = QUESTIONS[q_idx]
    buttons = []
    for idx, (ans_text, score_type) in enumerate(q["answers"]):
        buttons.append([InlineKeyboardButton(text=ans_text, callback_data=f"ans_{score_type}")])
        
    kb = InlineKeyboardMarkup(inline_keyboard=buttons)
    await bot.send_message(chat_id=chat_id, text=q["text"], reply_markup=kb)

async def send_question(message: Message, q_idx: int):
    q = QUESTIONS[q_idx]
    buttons = []
    for idx, (ans_text, score_type) in enumerate(q["answers"]):
        buttons.append([InlineKeyboardButton(text=ans_text, callback_data=f"ans_{score_type}")])
        
    kb = InlineKeyboardMarkup(inline_keyboard=buttons)
    await message.edit_text(q["text"], reply_markup=kb)

@router.callback_query(QuizStates.answering, F.data.startswith("ans_"))
async def handle_answer(callback: CallbackQuery, state: FSMContext):
    data = await state.get_data()
    q_idx = data["q_idx"]
    scores = data["scores"]
    
    score_type = callback.data.split("_")[1]
    scores[score_type] += 1
    
    next_idx = q_idx + 1
    await state.update_data(scores=scores, q_idx=next_idx)
    
    if next_idx < len(QUESTIONS):
        await send_question(callback.message, next_idx)
    else:
        best_profile = max(scores, key=scores.get)
        profile_text = PROFILES[best_profile]
        
        # Юзернейм менеджера для кнопки записи (замени на свой)
        cta_kb = InlineKeyboardMarkup(inline_keyboard=[
            [InlineKeyboardButton(text="Записаться на диагностику", url="https://t.me/your_manager_username")]
        ])
        
        await callback.message.edit_text(profile_text, parse_mode="Markdown")
        await callback.message.answer(CTA_TEXT, reply_markup=cta_kb)
        await state.clear()
        
    await callback.answer()

